<style lang="less">
    @import '../../../styles/common.less';
</style>

<template>
    <div>
        <Row class="margin-top-10">
            <Col span="24">
                <Card dis-hover>
                    <Row>
                        <Select style="width:200px" v-model="productId" v-show="productShow">
                            <Option v-for="item in productData" :value="item.id" :key="item.id">{{ item.name }}</Option>
                        </Select>
                        <div style="float: right;" >
                            <Button type="primary" @click="refresh()">Refresh</Button>
                        </div>
                    </Row>
                    <Row>
                        <hr class="hr-margin" color="#e3e8ee" size="0.5">
                    </Row>
                    <Row>
                        <Col span="24">
                            <Card dis-hover>
                                <Form ref="formValidate" :model="formValidate" :rules="ruleValidate" :label-width="47">
                                    <FormItem label="target" prop="target">
                                        <CheckboxGroup v-model="formValidate.target">
                                            <Table size="small" width="100%"  border :columns="columnsTarget" :data="targetData" stripe></Table>
                                        </CheckboxGroup>
                                    </FormItem>
                                    <FormItem label="command " prop="command">
                                        <Input v-model="formValidate.command" type="textarea" :autosize="{minRows: 2,maxRows: 5}" placeholder="Enter shell command"></Input>
                                    </FormItem>
                                    <FormItem>
                                        <Button type="primary" @click="handleSubmit('formValidate')">submit</Button>
                                        <Button type="ghost" @click="handleReset('formValidate')">Reset</Button>
                                        <div style="float: right">
                                            <Poptip placement="top-end" width="700">
                                                <Button type="ghost" @click="handleHistory">Historical order</Button>
                                                <div class="api" slot="content">
                                                    <div style="padding-bottom: 5px">
                                                        <Input v-model="searchConName" icon="search" @on-change="handleSearch" placeholder="Please enter a history command" style="width: 200px" />
                                                    </div>
                                                    <Table size="small" width="100%" height="190" border :columns="columnsHistory" :data="historyData" stripe></Table>
                                                </div>
                                             </Poptip>
                                        </div>
                                    </FormItem>
                                    <FormItem label="result">
                                        <Spin size="large" fix v-if="spinShow"></Spin>
                                        <Alert :type="summaryType">
                                            <ul>
                                                <li>
                                                    total: {{result.total}}
                                                </li>
                                                <li>
                                                    success: {{result.succeed}}
                                                </li>
                                                <li>
                                                    failure: {{result.failure}}
                                                </li>
                                                <li>
                                                    Failed host: {{result.failure_minion}}
                                                </li>
                                                <li>
                                                    command: {{result.command}}
                                                </li>
                                            </ul>
                                        </Alert>
                                        <highlight-code lang="yaml" v-show="resultShow" style="overflow:auto"　v-for="(item, minion) in result.result" :key="item.minion">
Minion: {{minion}}
{{item}}
                                        </highlight-code>
                                    </FormItem>
                                </Form>
                            </Card>
                        </Col>
                    </Row>
                </Card>
            </Col>
        </Row>
    </div>
</template>

<script>
    export default {
        name: 'Execute',
        data () {
            return {
                productData: this.productList(),
                productId: '',
                // 执行命令的返回数据
                result: '',
                searchConName: '',
                formValidate: {
                    command: '',
                    target: []
                },
                // 默认不显示结果信息
                resultShow: false,
                // 摘要信息样式
                summaryType: 'success',
                // 等待返回结果
                spinShow: false,
                // 全选
                target: [],
                ruleValidate: {
                    command: [
                        { required: true, message: 'Please enter a command', trigger: 'blur' }
                    ],
                    target: [
                        { required: true, type: 'array', message: 'Please tick the host or group', trigger: 'change' }
                    ]
                },
                columnsTarget: [
                    {
                        title: 'Name',
                        key: 'name',
                        width: 200,
                        render: (h, params) => {
                            return h('CheckboxGroup', [
                                h('Checkbox', {
                                    props: {
                                        label: params.row.name
                                    },
                                    nativeOn: {
                                        click: () => {
                                            if (this.formValidate.target.includes(params.row.name)) {
                                                this.formValidate.target.splice(this.formValidate.target.indexOf(params.row.name), 1);
                                                // 索引位置变化,采用逆向循环,达到数组去掉另一个数组元素的目的
                                                for (let i = this.formValidate.target.length - 1; i >= 0; i--) {
                                                    for (let m = params.row.minion.length - 1; m >= 0; m--) {
                                                        if (this.formValidate.target[i] === params.row.minion[m]) {
                                                            this.formValidate.target.splice(i, 1);
                                                        }
                                                    }
                                                }
                                            } else {
                                                this.formValidate.target.push(params.row.name);
                                                this.formValidate.target = this.formValidate.target.concat(params.row.minion);
                                            }
                                        }
                                    }
                                }, params.row.name)
                            ]);
                        }
                    },
                    {
                        title: 'Host',
                        key: 'minion',
                        render: (h, params) => {
                            return h('div', params.row.minion.map(item => {
                                return h('Checkbox', {
                                    props: {
                                        label: item
                                    }
                                }, item);
                            })
                            );
                        }
                    }
                ],
                targetData: [],
                columnsHistory: [
                    {
                        title: 'command ',
                        key: 'command',
                        render: (h, params) => {
                            return h('div', {
                                on: {
                                    'dblclick': () => {
                                        this.formValidate.command = params.row.command;
                                    }
                                }
                            }, params.row.command);
                        }
                    },
                    {
                        title: 'User',
                        key: 'username',
                        width: 180
                    },
                    {
                        title: 'Time',
                        key: 'time',
                        width: 160
                    }
                ],
                historyData: [],
                initHistoryData: []
            };
        },
        props: {
            apiService: {
                type: String,
                required: true
            },
            productShow: {
                type: Boolean
            }
        },
        watch: {
            // 监控产品线变化
            productId () {
                // 重新获取分组信息
                this.getGroups();
                // 清除命令表单数据
                this.formValidate.command = '';
                // 清除目标表单数据
                this.formValidate.target = [];
                // 清除结果信息
                this.resultShow = false;
                // 清除摘要信息
                this.result = '';
                // 停止loading
                this.spinShow = false;
                // 重新获取历史命令
                this.getHistory();
                // 清除搜索的内容
                this.searchConName = '';
            }
        },
        methods: {
            // 获取用户所拥有的产品线
            productList () {
                this.axios.get(this.Global.serverSrc + 'product').then(
                    res => {
                        if (res.data['status'] === true) {
                            this.productData = res.data['data'];
                            if (this.productData.length > 0) {
                                this.productId = this.productData[0].id;
                            }
                            // 加载完产品线后加载历史命令
                            this.getHistory();
                        } else {
                            this.nError('Get Product Failure', res.data['message']);
                        }
                    },
                    err => {
                        let errInfo = '';
                        try {
                            errInfo = err.response.data['message'];
                        } catch (error) {
                            errInfo = err;
                        }
                        this.nError('Get Product Failure', errInfo);
                    });
            },
            // 重新定义错误消息
            nError (title, info) {
                this.$Notice.error({
                    title: title,
                    desc: info,
                    duration: 10
                });
            },
            refresh () {
                this.getGroups();
                this.getHistory();
            },
            // 处理选择的minion
            handleTarget () {
                // 去重
                this.target = [...new Set(this.formValidate.target)];
                // 获取所有的组名
                let group = this.targetData.map(item => {
                    return item['name'];
                });
                // 去除minion里面的组名
                for (let i = this.target.length - 1; i >= 0; i--) {
                    for (let m = group.length - 1; m >= 0; m--) {
                        if (this.target[i] === group[m]) {
                            this.target.splice(i, 1);
                        }
                    }
                }
            },
            handleSubmit (name) {
                this.$refs[name].validate((valid) => {
                    if (valid) {
                        this.spinShow = true;
                        this.handleTarget();
                        let postData = {
                            'minion_id': this.target,
                            'command': this.formValidate.command
                        };
                        this.axios.post(this.Global.serverSrc + this.apiService + '?product_id=' + this.productId, postData).then(
                            res => {
                                if (res.data['status'] === true) {
                                    this.result = res.data['data'];
                                    this.resultShow = true;
                                    this.spinShow = false;
                                    // 有错误后显示不同的颜色
                                    if (this.result['failure'] !== '0') {
                                        this.summaryType = 'error';
                                    } else {
                                        this.summaryType = 'success';
                                    }
                                } else {
                                    this.result = '';
                                    this.resultShow = false;
                                    this.spinShow = false;
                                    this.nError('Execute Failure', res.data['message']);
                                }
                            },
                            err => {
                                let errInfo = '';
                                try {
                                    errInfo = err.response.data['message'];
                                } catch (error) {
                                    errInfo = err;
                                }
                                this.result = '';
                                this.resultShow = false;
                                this.spinShow = false;
                                this.nError('Execute Failure', errInfo);
                            });
                    } else {
                        this.$Message.error('Please check the form data!');
                    }
                });
            },
            handleReset (name) {
                this.$refs[name].resetFields();
            },
            getHistory () {
                this.axios.get(this.Global.serverSrc + 'history?type=shell&product_id=' + this.productId).then(
                    res => {
                        if (res.data['status'] === true) {
                            this.historyData = res.data['data'];
                            this.initHistoryData = this.historyData;
                        } else {
                            this.nError('Get Info Failure', res.data['message']);
                        };
                        this.loading = false;
                    },
                    err => {
                        let errInfo = '';
                        try {
                            errInfo = err.response.data['message'];
                            if (err.response.status === 404) {
                                this.historyData = [];
                            } else {
                                this.nError('Get Info Failure', errInfo);
                            }
                        } catch (error) {
                            errInfo = err;
                            this.nError('Get Info Failure', errInfo);
                        }
                        this.loading = false;
                    });
            },
            getGroups () {
                this.axios.get(this.Global.serverSrc + 'execute/groups?product_id=' + this.productId).then(
                    res => {
                        if (res.data['status'] === true) {
                            this.targetData = res.data['data'];
                        } else {
                            this.nError('Get Info Failure', res.data['message']);
                        }
                        ;
                        this.loading = false;
                    },
                    err => {
                        let errInfo = '';
                        try {
                            errInfo = err.response.data['message'];
                            if (err.response.status === 404) {
                                this.targetData = [];
                            } else {
                                this.nError('Get Info Failure', errInfo);
                            }
                        } catch (error) {
                            errInfo = err;
                            this.nError('Get Info Failure', errInfo);
                        }
                        this.loading = false;
                    });
            },
            search (data, argumentObj) {
                let res = data;
                let dataClone = data;
                for (let argu in argumentObj) {
                    if (argumentObj[argu].length > 0) {
                        res = dataClone.filter(d => {
                            return d[argu].indexOf(argumentObj[argu]) > -1;
                        });
                        dataClone = res;
                    }
                }
                return res;
            },
            handleSearch () {
                this.historyData = this.initHistoryData;
                this.historyData = this.search(this.historyData, {command: this.searchConName});
            },
            handleHistory () {
                this.searchConName = '';
                this.getHistory();
            }
        }
    };
</script>
